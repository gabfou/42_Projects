#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		j;
	char	*s2;

	j = ft_strlen(s1);
	s2 = malloc(sizeof(s2) * j);
	i = -1;
	while (++i < j)
		s2[i] = s1[i];
	s2[i] = s1[i];
	return (s2);
}
