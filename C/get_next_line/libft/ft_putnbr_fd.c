#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr_fd((n - (n % 10)) / 10, fd);
	ft_putchar_fd((n % 10) + 48, fd);
}
