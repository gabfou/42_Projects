#include "libft.h"

void ft_putendl_fd(char const *s, int fd)
{
	int i;

	if (s == NULL)
		return ;
	i = -1;
	while (s[++i] != 0)
		write(1, &s[i], fd);
	write(1, "\n", 1);
}
